import fs from "fs";
import path from "path";
import Papa from "papaparse";

export interface RaceInfo {
  round: number;
  name: string;
  year: number;
  dirName: string;
}

/**
 * Scans the reports directory to find all available races.
 */
export function getAvailableRaces(year: number): RaceInfo[] {
  const yearDir = path.join(getReportsDirectory(), year.toString());
  if (!fs.existsSync(yearDir)) return [];

  // In our structure, Miami is a folder, and summaries are in another folder.
  // We'll define a mapping or look for specific markers.
  // For this project, let's look at the summaries folder and extract round numbers.
  const summariesDir = path.join(yearDir, "summaries");
  if (!fs.existsSync(summariesDir)) return [];

  const files = fs.readdirSync(summariesDir);
  const rounds = new Set<number>();
  files.forEach(f => {
    const match = f.match(/round_(\d+)/);
    if (match && match[1]) rounds.add(parseInt(match[1]));
  });

  // Map rounds to names (Full 2026 Season Mapping)
  const roundNames: Record<number, { name: string; dir: string }> = {
    1: { name: "Bahrain Grand Prix", dir: "Bahrain_Grand_Prix" },
    2: { name: "Saudi Arabian Grand Prix", dir: "Saudi_Arabian_Grand_Prix" },
    3: { name: "Australian Grand Prix", dir: "Australian_Grand_Prix" },
    4: { name: "Miami Grand Prix", dir: "Miami_Grand_Prix" },
    5: { name: "Canadian Grand Prix", dir: "Canadian_Grand_Prix" },
    6: { name: "Spanish Grand Prix", dir: "Spanish_Grand_Prix" },
    7: { name: "Austrian Grand Prix", dir: "Austrian_Grand_Prix" },
    8: { name: "British Grand Prix", dir: "British_Grand_Prix" },
    9: { name: "Hungarian Grand Prix", dir: "Hungarian_Grand_Prix" },
    10: { name: "Belgian Grand Prix", dir: "Belgian_Grand_Prix" },
  };

  return Array.from(rounds).sort((a, b) => a - b).map(r => ({
    round: r,
    name: roundNames[r]?.name || `Round ${r}`,
    year,
    dirName: roundNames[r]?.dir || `Round_${r}`
  }));
}

/**
 * Returns the absolute path to the reports directory.
 * Works both locally and on Vercel.
 */
function getReportsDirectory() {
  // process.cwd() in Next.js is usually the `dashboard` folder
  return path.join(process.cwd(), "..", "reports");
}

export interface PredictionRow {
  Driver: string;
  Team: string;
  predicted_laptime_xgb_s: string;
  predicted_position?: number;
}

/**
 * Reads a Markdown file generated by the AI Summarizer.
 */
export function getRaceSummary(year: number, eventDirName: string, roundNum: number): string | null {
  try {
    const filePath = path.join(
      getReportsDirectory(),
      year.toString(),
      "summaries",
      `report_round_${roundNum}.md`
    );
    
    if (!fs.existsSync(filePath)) {
      return null;
    }
    
    return fs.readFileSync(filePath, "utf8");
  } catch (error) {
    console.error("Failed to read race summary:", error);
    return null;
  }
}

