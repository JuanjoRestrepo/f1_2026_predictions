/**
 * Reads the predictions CSV file.
 */
export function getRacePredictions(year: number, eventDirName: string): PredictionRow[] | null {
  try {
    const filePath = path.join(
      getReportsDirectory(),
      year.toString(),
      eventDirName,
      "results",
      "data",
      "predictions.csv"
    );
    
    if (!fs.existsSync(filePath)) {
      return null;
    }
    
    const csvFile = fs.readFileSync(filePath, "utf8");
    const parsed = Papa.parse<PredictionRow>(csvFile, {
      header: true,
      skipEmptyLines: true,
    });
    
    // Sort by predicted laptime ascending to get finishing order
    const sorted = parsed.data
      .filter(r => r.Driver && r.predicted_laptime_xgb_s)
      .sort((a, b) => parseFloat(a.predicted_laptime_xgb_s) - parseFloat(b.predicted_laptime_xgb_s))
      .map((row, idx) => ({ ...row, predicted_position: idx + 1 }));

    return sorted;
  } catch (error) {
    console.error("Failed to read predictions CSV:", error);
    return null;
  }
}

export interface ActualResult {
  position: number;
  driver: string;
  team: string;
  time: string;
  gap: string;
}

/**
 * Reads the actual race results JSON generated post-race.
 */
export function getActualResults(year: number, roundNum: number): ActualResult[] | null {
  try {
    const filePath = path.join(
      getReportsDirectory(),
      year.toString(),
      "summaries",
      `actual_results_round_${roundNum}.json`
    );
    if (!fs.existsSync(filePath)) return null;
    const raw = fs.readFileSync(filePath, "utf8");
    return JSON.parse(raw) as ActualResult[];
  } catch (error) {
    console.error("Failed to read actual results:", error);
    return null;
  }
}
